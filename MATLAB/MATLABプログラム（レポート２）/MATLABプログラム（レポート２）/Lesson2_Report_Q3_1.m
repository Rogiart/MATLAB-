% 正解データ「actid」: 座る(1), 立つ(2), 歩く(3)  actid(4030:6520,1)
% 特徴量「feat」: 加速度のx成分の平均値（1列） feat(4030:6520,1)
% 　　　　　　　　加速度のy成分の平均値（2列） feat(4030:6520,2)
% 　　　　　　　　加速度のz成分の平均値（3列） feat(4030:6520,3)
