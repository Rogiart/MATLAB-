load humanactivity.mat
Z = linkage(feat(4030:6520,1:3),'average','chebychev');
T = cluster(Z,'cutoff',0.117,'Criterion','distance');
length(unique(T));
gscatter(feat(4030:6520,1),feat(4030:6520,3), T, 'rmb', '+o.')
xlabel('$ \bar{a}_x$','FontSize',14,'Interpreter','latex')
ylabel('$ \bar{a}_z$','FontSize',14,'Interpreter','latex')
